/*
Escribe un programa que compare dos números y muestre un mensaje indicando cuál es el mayor.
*/
let NumeroUno = prompt("Ingrese su primer número")
let NumeroDos = prompt("Ingrese su segundo número")

if (NumeroUno > NumeroDos) {
    alert(NumeroUno + " es mayor")
} else if (NumeroUno < NumeroDos) {
    alert(NumeroDos + " es mayor")
} else{
    alert("Son iguales")
}

